    public Boolean validPlacement(Point posPoint)
    {
        // If (x, y) == '.' -> valid placement(true)
        if (mazeArray[posPoint.y][posPoint.x] == '.')
        {
            return true;
        }
        // Else all other (x, y) are walls/starts/exit/keys/bots... or OOB
        else
        {
            return false;
        }
    }
